print("type of x",type(x))
print("------------------------------------------------Numeric Types------------------------------------------------------------------")
print("----------------------int---------------------------")
x = 20
print("x =",x)
print("type of x",type(x))
print("----------------------------------------------------")
print("----------------------folat-------------------------")
x = 2.43
print("x =",x)
print("type of x",type(x))
print("----------------------------------------------------")
print("----------------------complex-----------------------")
x = 2j
print("x =",x)
print("type of x",type(x))
print("----------------------------------------------------")
print("---------------------------------------------------Sequence Types:----------------------------------------------------------------")

print("----------------------List--------------------------")
x = ["omkar","arun","nale"]
print("x =",x)
print("type of x =",type(x))
print("----------------------------------------------------")

print("----------------------Tuple--------------------------")
x = ("omkar","arun","nale")
print("x =",x)
print("type of x =",type(x))
print("----------------------------------------------------")

print("-------------Range(num or variable)-----------------")
x = range(6)
print("x =",x)
print("type of x =",type(x))
print("----------------------------------------------------")

print("---------------------------------------------------Mapping Type:-------------------------------------------------------")

print("-------------dict-----------------")
x = {"id":"235","name":"omkar"}
print("x =",x)
print("type of x =",type(x))
print("----------------------------------------------------")

print("-------------------------------------------------------Set Types:-------------------------------------------------------")

print("-------------------------set-------------------------")
x = {234,"omkar",23.56}
print("x =",x)
print("type of x =",type(x))
print("------------------------------------------------------")

print("-------------------------frozenset--------------------")
x = frozenset({234,"omkar",23.56})

print("type of x =",type(x))
print("------------------------------------------------------")

print("----------------------------------------------------Boolean Type:--------------------------------------------------------")

print("----------------------bool----------------------------")
x = True
print("x =",x)
print("type of x =",type(x))
print("------------------------------------------------------")

print("----------------------------------------------------Binary Types:--------------------------------------------------------")

print("----------------------bytes----------------------------")
x = b"x"
print("x =",x)
print("type of x =",type(x))
print("------------------------------------------------------")

